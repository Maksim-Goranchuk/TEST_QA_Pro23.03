// 1. 'number' + 3 + 3
// Результат: "number33"
// Логика: Слева направо. Строка + число = строка ("number3"). Затем "number3" + 3 = "number33".

// 2. null + 3
// Результат: 3
// Логика: null при математических операциях преобразуется в 0. 0 + 3 = 3.

// 3. 5 && "qwerty"
// Результат: "qwerty"
// Логика: Оператор && возвращает последнее истинное значение, если оба операнда true. 5 — это true.

// 4. +'40' + +'2' + "hillel"
// Результат: "42hillel"
// Логика: Унарный плюс преобразует строки в числа. 40 + 2 = 42. Затем число 42 + строка "hillel" = "42hillel".

// 5. '10' - 5 === 6
// Результат: false
// Логика: Минус преобразует '10' в число. 10 - 5 = 5. 5 строго равно 6? Нет, false.

// 6. true + false
// Результат: 1
// Логика: true преобразуется в 1, false в 0. 1 + 0 = 1.

// 7. '4px' - 3
// Результат: NaN
// Логика: Математическая операция со строкой, которая не является чистым числом, дает NaN (Not a Number).

// 8. '4' - 3
// Результат: 1
// Логика: Минус преобразует строку '4' в число 4. 4 - 3 = 1.

// 9. '6' + 3 ** 0
// Результат: "61"
// Логика: Приоритет у возведения в степень. 3**0 = 1. Затем строка '6' + число 1 = "61".

// 10. 12 / '6'
// Результат: 2
// Логика: Деление преобразует строку '6' в число. 12 / 6 = 2.

// 11. '10' + (5 === 6)
// Результат: "10false"
// Логика: Сначала в скобках. 5 === 6 — это false. Затем строка '10' + булево false = "10false".

// 12. null == ''
// Результат: false
// Логика: null равен только undefined при нестрогом сравнении. С пустой строкой он не равен.

// 13. 3 ** (9 / 3)
// Результат: 27
// Логика: Сначала в скобках: 9/3 = 3. Затем 3 в кубе (3 * 3 * 3) = 27.

// 14. !!'false' == !!'true'
// Результат: true
// Логика: Любая непустая строка — это true. !!'false' преобразуется в true. true == true — это true.

// 15. 0 || '0' && 1
// Результат: 1
// Логика: У && приоритет выше. '0' — это true, поэтому '0' && 1 возвращает 1. Затем 0 || 1 возвращает 1.

// 16. (+null == false) < 1
// Результат: false
// Логика: +null это 0. 0 == false — это true. Получаем true < 1. true преобразуется в 1. 1 < 1 — это false.

// 17. false && true || true
// Результат: true
// Логика: Приоритет у &&. false && true = false. Затем false || true = true.

// 18. false && (false || true)
// Результат: false
// Логика: В скобках false || true = true. Затем false && true = false.

// 19. (+null == false) < 1 ** 5
// Результат: false
// Логика: (+null == false) это true (как в примере 16). 1**5 это 1. true < 1 преобразуется в 1 < 1 = false.
